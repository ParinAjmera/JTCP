<h4>Who Should Attend</h4>

<p>This course has been developed for anyone seeking a basic understanding of hot-mix asphalt pavements. It is intended to focus on areas critical to the successful completion of an asphalt pavement.</p>

<h4>Completing the Program</h4>

<p>This certificate program consists of completing an online prerequisite, in-class training, and a lab.</p>

<p>The timely delivery of transportation improvement projects requires highly skilled, knowledgeable materials sampling and testing technicians. To help reduce conflict and delay on construction projects within the state, the California Department of Transportation (Caltrans), in partnership with CSULB, has developed a joint training and certification program in Hot Mix Asphalt (HMA) testing for construction materials lab technicians. This program is the first in a two-part series.</p>

<p>The goal of this program is to ensure that all materials testers — both in the public and private sector — possess equal knowledge and skill in performing lab tests. This will, in turn, help ensure consistent testing, promote quality improvement, and foster positive working relationships between public and private sector employees.</p>

<p>Participants in this program will receive training and certification in various California Test Methods (CTM) and American Association of State Highway and Transportation Officials (AASHTO) methods pertaining to HMA testing in the field.</p>

<h4>Completing the Prerequisite</h4>

<p>To complete the prerequisite, students must review provided resource material, view course videos, and pass an assessment quiz. Students must successfully pass the prerequisite course before registering in the Training & Lab course.</p>

<h4>Topics Include</h4>

<ul>
<li>CT 105: Calculations Pertaining to Gradings and Specific Gravities</li>
<li>CT 125: Method of Test for Sampling Highway Materials and Products Used in the Roadway Structural Sections</li>
<li>AASHTO T 11: Materials Finer Than 75-µm (No. 200) Sieve in Mineral Aggregates by Washing</li>
<li>AASHTO T 27: Sieve Analysis of Fine and Coarse Aggregates</li>
<li>AASHTO T 176: Plastic Fines in Graded Aggregates and Soils by Use of the Sand Equivalent Test</li>
<li>AASHTO T 248: Reducing Samples of Aggregate to Testing Size</li>
<li>AASHTO T 255: Total Evaporable Moisture Content of Aggregate by Drying</li>
<li>AASHTO T 329: Determine the Moisture Content of HMA by Drying in an Oven</li>
<li>AASHTO T 335: Determine the Percentage of Fracture in Coarse Aggregate</li>
<li>AASHTO R 47: Reducing Samples of HMA to Testing Size</li>
</ul>

<h4>Upon completion of this training, participants will be able to:</h4>

<ul>
<li>Discuss the reasons for performing grading adjustments, select the appropriate adjustment for a given sample, and describe the procedure involved</li>
<li>Sample aggregates, soil, lime, hot mix asphalt, and bituminous materials</li>
<li>Determine the amount passing sieve No. 200 of an aggregate sample and be aware of the safety requirements for running the test</li>
<li>Determine the aggregate gradation of fine or coarse aggregates</li>
<li>Determine the relative proportion of clay-like material in soils or graded aggregates</li>
<li>Reduce the aggregate sample to a representative size for testing</li>
<li>Determine the total evaporable moisture content of aggregate</li>
<li>Determine the moisture content of HMA</li>
<li>Determine the percentage of Fracture in Coarse Aggregate</li>
<li>Reduce the sample size of HMA and be aware of the safety requirements for running the test</li>
</ul>

