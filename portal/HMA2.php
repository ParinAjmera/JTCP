<h4>Who Should Attend</h4>

<p>This program has been developed for anyone seeking a basic understanding of hot-mix asphalt pavements. It is intended to focus on areas critical to the successful completion of an asphalt pavement.</p>

<h4>Completing the Program</h4>

<p>This certificate program consists of completing an online prerequisite, in-class training, and a lab.</p>

<p>The timely delivery of transportation improvement projects requires highly skilled, knowledgeable materials sampling and testing technicians. To help reduce conflict and delay on construction projects within the state, the California Department of Transportation (Caltrans), in partnership with CSULB, has developed a joint training and certification program in Hot Mix Asphalt (HMA) testing for construction materials lab technicians. This program is the second in a two-part series.
The goal of this program is to ensure that all materials testers — both in the public and private sector — possess equal knowledge and skill in performing lab tests. This will, in turn, help ensure consistent testing, promote quality improvement, and foster positive working relationships between public and private sector employees.</p>

<p>Participants in this program will receive training and certification in various American Association of State Highway and Transportation Officials (AASHTO) methods pertaining to HMA testing in the field.</p>

<h4>Completing the Prerequisite</h4>

<p>To complete the prerequisite students must review provided resource material, view course videos, and pass an assessment quiz. Students must successfully pass the prerequisite course before registering in the Training & Lab course.</p>

<h4>Topics Include</h4>

<ul>
<li>AASHTO T166: Standard Method of Test for Bulk Specific Gravity (Gmb) of Compacted HMA Using Saturated Surface-Dry Method</li>
<li>AASHTO T209: Standard Method of Test for Theoretical Maximum Specific Gravity (Gmm) and Density of HMA (Method A)</li>
<li>AASHTO T269: Standard Method of Test for Percent Air Voids in Compacted Dense and Open Asphalt Mixtures Synopsis</li>
<li>AASHTO T275: Standard Method of Test for Bulk Specific Gravity (Gmb) of Compacted HMA Using Paraffin-Coated Specimens Synopsis</li>
<li>AASHTO T308: Standard Method of Test for Determining the Asphalt Binder Content of HMA by the Ignition Method (Method A)</li>
</ul>

<h4>Upon completion of this training, participants will be able to:</h4>

<ul>
<li>Recognize the safety issues associated with each test</li>
<li>Become familiar with HMA mixtures terminology</li>
<li>Recognize the significant impact of temperature and rate of loading on the properties of HMA mixtures</li>
<li>Appreciate the importance of proper calibration of laboratory equipment to maintain the required temperature and rate of loading</li>
<li>Differentiate between test method precision and bias</li>
<li>Recognize relationships between mix design parameters and HMA performance</li>
<li>Recognize the role of QA/QC in the field management process</li>
<li>Recognize the fundamental relationships between weights and volumes of aggregates and HMA mixtures</li>
<li>Define HMA mixture volumetric parameters and relationships among them</li>
<li>Measure the various weights and volumes of the compacted HMA mix</li>
<li>Calculate the bulk specific gravity of the compacted HMA mix</li>
<li>Measure the various weights and volumes of the un-compacted HMA mix</li>
<li>Calculate the maximum specific gravity of the un-compacted HMA mix</li>
<li>Calculate the percent air voids in the compacted asphalt mix</li>
<li>Measure the various asphalt binder content of field-produced HMA mix</li>
<li>Adjust the measured asphalt binder based on the specific conditions of the ignition oven and HMA mix</li>
</ul>

