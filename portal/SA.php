<h4>Who Should Attend</h4>

<p>This course has been developed for anyone seeking a basic understanding of hot-mix asphalt pavements. It is intended to focus on areas critical to the successful completion of an asphalt pavement.</p>

<h4>Completing the Program</h4>

<p>This certificate program consists of completing an online prerequisite, in-class training, and a lab.</p>

<p>The timely delivery of transportation improvement projects requires highly skilled, knowledgeable materials sampling and testing technicians. To help reduce conflict and delay on construction projects within the state, the California Department of Transportation (Caltrans), in partnership with CSULB, has developed a joint training and certification program in Soils and Aggregates testing for construction materials lab technicians.</p>

<p>The goal of this program is to ensure that all materials testers — both in the public and private sector — possess equal knowledge and skill in performing lab tests. This will, in turn, help ensure consistent testing, promote quality improvement, and foster positive working relationships between public and private sector employees.Participants in this program will receive training and certification in various California Test Methods (CTM) and American Society for Testing and Materials (ASTM) methods pertaining to Soils and Aggregates testing in the field.</p>

<h4>Completing the Prerequisite</h4>

<p>To complete the prerequisite students must review provided resource material, view course videos, and pass an assessment quiz.</p>

<h4>Topics Include</h4>

<ul>
<li>CT 105: Calculations Pertaining to Gradings and Specific Gravities</li>
<li>CT 201: Method of Test for Soil and Aggregate Preparation</li>
<li>CT 125: Method of Test for Sampling Highway Materials and Products Used in the Roadway Structural Sections</li>
<li>CT 202: Method of Test for Sieve Analysis of Fine and Coarse Aggregate</li>
<li>CT 226: Method of Test for Determination of Moisture Content of Soils and Aggregates by Oven Drying</li>
<li>CT 205 : Method of Test for Determining Percentage of Crushed Particles
ASTM D1557: Standard Test Method for Laboratory Compaction Characteristics of Soil Using Modified Effort</li>
<li>CT 217: Method of Test for Sand Equivalent</li>
<li>CT 227: Method of Test for Evaluating Cleanness of Coarse Aggregate</li>
<li>CT 229: Method of Test for Durability Index</li>
</ul>

<h4>Upon completion of this training, participants will be able to:</h4>

<ul>
<li>Prepare specimens for testing</li>
<li>Select the appropriate test method</li>
<li>Identify equipment and materials used to run the tests</li>
<li>Learn the precautions taken during the tests</li>
<li>Describe the safety requirements during running the tests</li>
<li>Explain common testing errors and how to avoid them</li>
</ul>

<h4>As well as:</h4>

<li>Discuss the reasons for performing grading adjustments, select the appropriate adjustment for a given sample, and describe the procedure involved</li>
<li>Divide a sample in coarse and fine fractions, pulverize clods and remove coatings from coarse aggregate, and reduce samples to representative test specimens</li>
<li>Sample aggregates, soil, lime, hot mix asphalt, and bituminous materials</li>
<li>Run the sieve analysis test for both fine and coarse aggregate</li>
<li>Calculate and adjust the percentage passing from each sieve</li>
<li>Define wet weight and constant weight, understand methods to reduce drying time, and calculate and report moisture content</li>
<li>Realize the difference between fractured surface and non-fractured surface</li>
<li>Determine the percentage of crushed particles in an aggregate sample</li>
<li>Calculate dry density and unit weight and molding water content</li>
<li>Create a compaction curve and evaluate the modified maximum dry unit weight and optimum molding water content</li>
<li>Perform Sand Equivalent (SE) tests on various types of construction materials, calculate SE results and apply correction factors if necessary</li>
<li>Test, calculate and report Cleanness Value (CV)</li>
<li>Perform the Durability Index (DI) test on various aggregates, calculate and report DI.</li>

